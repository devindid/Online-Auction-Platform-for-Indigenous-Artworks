export const convert_currency = (amount) => {
    const loggedInUser = JSON.parse(localStorage.getItem("user"));

    if (!loggedInUser) {
        return {
            value: amount,
            symbol: "$"
        };
    }

    const from_currency = CURRENCIES.find(c => c.name === "USD");
    const to_currency = CURRENCIES.find(c => c.name === loggedInUser.currency);

    let value = amount * (to_currency.value / from_currency.value);
    value = value.toFixed(2);

    return {
        value,
        symbol: to_currency.symbol
    };
}
